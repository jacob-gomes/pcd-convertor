package com.pdfcore.main.exceptions;

public class ConfigInitRuntimeException extends RuntimeException {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1845858580064833714L;

	public ConfigInitRuntimeException() {
		super();
	}

	public ConfigInitRuntimeException(String arg0) {
		super(arg0);
	}

	public ConfigInitRuntimeException(Throwable arg0) {
		super(arg0);
	}

	public ConfigInitRuntimeException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
