package com.pdfcore.main.exceptions;

public class FormMappingException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 734620320930645173L;

	public FormMappingException() {

	}

	public FormMappingException(String arg0) {
		super(arg0);

	}

	public FormMappingException(Throwable arg0) {
		super(arg0);

	}

	public FormMappingException(String arg0, Throwable arg1) {
		super(arg0, arg1);

	}

}
