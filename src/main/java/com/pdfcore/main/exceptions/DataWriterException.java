package com.pdfcore.main.exceptions;

public class DataWriterException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -6930636726100773808L;

	public DataWriterException() {}

	public DataWriterException(String arg0) {
		super(arg0);
	}

	public DataWriterException(Throwable arg0) {
		super(arg0);
	}

	public DataWriterException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
